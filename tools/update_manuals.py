#!/usr/bin/python3
"""
Update manuals within the SVN repository from a folder containing the manuals
with different covers, generated with generate_manuals.py.

This script is responsible for updating the pdfs within the setup folder,
with the manuals generated by the generate_manuals.py, in the user_manuals
repository.

This script can either be run stand-alone or used as a library. In stand-alone:

python3 update_manuals <path_to_repository_root> <path_to_manuals_root>
"""
