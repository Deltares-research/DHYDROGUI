namespace DeltaShell.Plugins.FMSuite.FlowFM.Gui.Editors
{
    /// <summary>
    /// The tabbed model editor ('Domain', 'Numerical Parameters', ..) under the central map. Its content is 95% autogenerated generated 
    /// from the schema csv, some buttons are added below manually.
    /// </summary>
    public partial class WaterFlowFMModelView : UserControl, ILayerEditorView, ITabbedModelView
    {
        private WaterFlowFMModel data;
        private DataEditor generatedEditor;
        private SetCoordinateSystemButton coordinateSystemButton;
        private EditCoverageLayersButton editSalinityLayersButton;

        private readonly string[] categoriesToSkip = new string[0];
        private IGui gui;

        public WaterFlowFMModelView()
        {
            InitializeComponent();
        }

        public IGui Gui
        {
            get { return gui; }
            set
            {
                gui = value;
                if (coordinateSystemButton != null)
                {
                    coordinateSystemButton.Gui = value;
                }
            }
        }

        public object Data
        {
            get { return data; }
            set
            {
                if (data != null)
                {
                    ((INotifyPropertyChange)data).PropertyChanged -= OnModelPropertyChanged;

                    generatedEditor.Data = null;
                    Controls.Remove(generatedEditor);
                    generatedEditor.Dispose();
                    generatedEditor = null;
                }

                data = (WaterFlowFMModel) value;

                if (data != null)
                {
                    ((INotifyPropertyChange)data).PropertyChanged += OnModelPropertyChanged;

                    Text = "Settings: " + data.Name;

                    coordinateSystemButton = new SetCoordinateSystemButton
                        {
                            Gui = Gui,
                            CoordinateSystemFilter = WaterFlowFMModel.IsValidCoordinateSystem
                        };

                    editSalinityLayersButton = new EditCoverageLayersButton
                    {
                        DepthLayersPrefix = data.InitialSalinity.Name
                    };

                    var descriptionExtractor = new WaterFlowFMGuiPropertyExtractor(data);

                    var objectDescription = descriptionExtractor.ExtractObjectDescription(categoriesToSkip);

                    objectDescription.FieldDescriptions
                        // add to begin:
                        = new[]
                        {
                            new FieldUIDescription(d => this, null)
                            {
                                Category = "General",
                                CustomControlHelper = new EditDepthLayersButton()
                            },
                            new FieldUIDescription(d => this, null)
                            {
                                Category = "General",
                                CustomControlHelper = coordinateSystemButton
                            },
                        }
                            // normal properties:
                            .Concat(objectDescription.FieldDescriptions)
                            // add to end:
                            .Concat(new[]
                            {
                                new FieldUIDescription(d => this, null)
                                {
                                    Category = "Toolboxes",
                                    CustomControlHelper = new FMToolboxesPanel(),
                                },
                                new FieldUIDescription(d => data.InitialSalinity, null)
                                {
                                    Category = "Initial Conditions",
                                    SubCategory = "Salinity",
                                    CustomControlHelper = editSalinityLayersButton,
                                },
                                new FieldUIDescription(d => data.TracerDefinitions, null)
                                {
                                    Category = "Processes",
                                    SubCategory = "Tracers",
                                    CustomControlHelper = new EditTracersControlHelper(),
                                },
                            }).ToList();

                    objectDescription.FieldDescriptions.First(f => f.Name == "StopTime").ValidationMethod =
                        (m, t) =>
                        ((WaterFlowFMModel) m).StartTime < (DateTime) t
                            ? ""
                            : "Start time must be smaller than stop time";

                    objectDescription.FieldDescriptions.First(f => f.Name == "AngLat").VisibilityMethod =
                        o => (data.CoordinateSystem == null || !data.CoordinateSystem.IsGeographic);

                    objectDescription.FieldDescriptions.First(f => f.Name == "AngLon").VisibilityMethod =
                        o => (data.CoordinateSystem == null || !data.CoordinateSystem.IsGeographic);

                    generatedEditor = DataEditorGeneratorSwf.GenerateView(objectDescription);

                    // for all time spans, include tenths of seconds:
                    foreach (var binding in generatedEditor.Bindings.Where(b => b.FieldDescription.ValueType == typeof (TimeSpan)))
                        ((TimeSpanEditor)binding.EditControl).IncludeTensOfSeconds = true;

                    // for all data/times, include seconds and tenths of seconds:
                    foreach (var binding in generatedEditor.Bindings.Where(b => b.FieldDescription.ValueType == typeof(DateTime)))
                        ((DateTimePicker)binding.EditControl).CustomFormat = RegionalSettingsManager.DateTimeFormat;
                    
                    // for all floating point values: use culture invariant representation:
                    foreach (
                        var source in
                            generatedEditor.Bindings.Where(b => b.FieldDescription.ValueType.IsNumericalType()))
                    {
                        source.FieldDescription.Culture = CultureInfo.InvariantCulture;
                    }

                    generatedEditor.Dock = DockStyle.Fill;
                    generatedEditor.Data = data;
                    Controls.Add(generatedEditor);
                }
            }
        }

        private static readonly string CoordinateSystemPropertyName = TypeUtils.GetMemberName<WaterFlowFMModel>(m => m.CoordinateSystem);

        private void OnModelPropertyChanged(object sender, PropertyChangedEventArgs args)
        {
            if (args.PropertyName == CoordinateSystemPropertyName && coordinateSystemButton != null)
            {
                coordinateSystemButton.UpdateLabelText();
            }
        }

        public Image Image { get; set; }

        public void EnsureVisible(object item)
        {
            if (item is string)
            {
                SwitchToTab((string) item);
            }
        }

        public ViewInfo ViewInfo { get; set; }

        public void SwitchToTab(string tabTitle)
        {
            var tabControl = generatedEditor.Controls.OfType<TabControl>().First();
            foreach (TabPage tab in tabControl.TabPages)
            {
                if (tab.Text.Equals(tabTitle))
                {
                    tabControl.SelectTab(tab);
                    return;
                }
            }
        }
        
        public IEnumerable<IFeature> SelectedFeatures { get; set; }
        public event EventHandler SelectedFeaturesChanged;
        public ILayer Layer { set; get; }
        public void OnActivated()
        {
        }

        public void OnDeactivated()
        {
        }
    }
}
